def identifier_developpeur(langage):
    if langage == "JavaScript":
        print("Tu es un développeur web")
    elif langage == "Python":
        print("Tu es un développeur IA")
    elif langage == "Java":
        print("Tu es un développeur logiciel")
    else:
        print("Tu es un développeur, mais le langage n'est pas spécifié dans les cas prévus")

# Appels de la fonction avec des paramètres différents
identifier_developpeur("JavaScript")
identifier_developpeur("Python")
identifier_developpeur("Java")
identifier_developpeur("C++")
